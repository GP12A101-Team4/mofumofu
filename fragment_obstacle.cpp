void UninitObstacleFragment(void)
{
    if (g_VertexBuffer) {
        g_VertexBuffer->Release();
        g_VertexBuffer = NULL;
    }

    for (int t = 0; t < OBSTACLE_TYPES; t++) {
        for (int i = 0; i < TEXTURE_PER_TYPE; i++) {
            if (g_Texture[t][i]) {
                g_Texture[t][i]->Release();
                g_Texture[t][i] = NULL;
            }
        }
    }
}

